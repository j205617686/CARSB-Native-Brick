package com.example.max241.fragmentunitest;

public class ComponentServiceReplacement {
	
	String task;
	String url;
	public ComponentServiceReplacement()
	{
		
		
	}
	public ComponentServiceReplacement(String task,String url)
	{
		this.task=task;
		this.url=url;
		
	}
	
	public String getTask() {
		return task;
	}
	public void setTask(String task) {
		this.task = task;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	
	

}
